DECLARE 
CURSOR cur_emple
IS 
SELECT *
FROM EMPLOYEES;
REG_EMPLE employees%ROWTYPE;

BEGIN
if not cur_emple%ISOPEN THEN 
OPEN cur_emple;

END IF;
LOOP

FETCH cur_emple INTO REG_EMPLE;

EXIT WHEN cur_emple%NOTFOUND;



DBMS_OUTPUT.PUT_LINE(REG_EMPLE.salary);

END LOOP;
CLOSE cur_emple;
END;



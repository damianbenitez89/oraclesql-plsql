SELECT R.REGION_NAME , C.COUNTRY_NAME
FROM REGIONS
INNER JOIN COUNTRIES
ON R.REGION_ID = C.REGION_ID
ORDER BY REGION_ID




SELECT R.REGION_NAME , C.COUNTRY_NAME
FROM REGIONS R
RIGHT OUTER  JOIN COUNTRIES C 
ON R.REGION_ID = C.REGION_ID
ORDER BY R.REGION_ID,
C.COUNTRY_NAME;
